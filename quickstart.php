<h2>Five Minute Quickstart Guide</h2>
<p>This five-minute quickstart guide will guide you through the steps to
  get your name on the global leaderboard in less than five minutes. There
  is also a Next Steps section to give you some idea which direction to go
  after you've completed this guide.</p>
<h3>1. Create an Account</h3>
<p>Create an account by filling out the <a href="register.php">signup form
  </a>.</p>
<h3>2. Activate Your Account</h3>
<p>Once your account is created, you must activate it by clicking the link in
  the confirmation email. If you haven't received a confirmation email in your
  inbox after five minutes, check your junk mail folder.</p>
<h3>3. Sign in to the Website</h3>
<p>You can sign in by clicking the <a href="login.php">Sign In</a> link at the
  top of the website.</p>
<h3>4. Download a Starter Package</h3>
<p>The starter packages are <a href="starter_packages.php">here</a>.</p>
<p>At this stage, it doesn't matter which starter package you choose, since
  we won't be modifying it yet. Choose any starter package, and download it.
  The starter packages are all ZIP files, and you should now have one of these
  ZIP files on your computer.</p>
<h3>5. Submit the Starter Package</h3>
<p>Don't bother unzipping or modifying the starter package you just downloaded.
  Make sure that you're <a href="login.php">signed in</a> to the website,
  visit the <a href="submit.php">Upload Your Code</a> page, and submit the
  starter package (ZIP file) that you just downloaded.</p>
<h3>6. You're Done!</h3>
<p>Since you've submitted an entry, your name will soon appear on the global
  leaderboard. It can take up to an hour for your name to appear, but usually
  only takes a few minutes.</p>

