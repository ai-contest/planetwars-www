<h2>Starter Packages</h2>
<p>Here are all the starter packages that we currently offer. Each starter
  package is a ZIP file which contains a simple already-working entry. You
  can even submit one of these starter packages as-is without modifying it.</p>
<p>If you want to use a programming language that's not listed here, give us
  a shout on the forums, and we would be happy to whip up a starter package
  for your language to help you get going. If you really know what you're
  doing, you can write your own.</p>

